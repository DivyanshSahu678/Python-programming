#Function Arguments

def average(a = 9, b = 1): #default arguments
    print("The Average is  ", (a+b)/2)

average(10) #Positional Arguments

def name(fname, mname = "jhon", lname = "doe"): #default arguments
    print("The name is ", fname, mname, lname) 

name("jane", "smith") #Positional Arguments

