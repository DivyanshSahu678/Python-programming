#Docstring in python

def square(n):
    """This function returns the square of a number n"""
    print(n * n)
square(5)
print(square.__doc__)

#doc string is write below the function name and it is used to describe the function and its purpose. It is a good practice to use docstring in your code to make it more readable and understandable.

